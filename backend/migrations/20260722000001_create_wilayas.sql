    code       SMALLINT PRIMARY KEY CHECK (code BETWEEN 1 AND 58),
    name_ar    TEXT NOT NULL,
    name_fr    TEXT NOT NULL,
    name_en    TEXT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

